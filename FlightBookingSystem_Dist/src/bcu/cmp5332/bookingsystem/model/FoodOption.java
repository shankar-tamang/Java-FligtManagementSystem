package bcu.cmp5332.bookingsystem.model;

/**
 * An enumeration representing different food options customers can choose for a flight booking.
 */
public enum FoodOption {
    /**
     * Indicates no in-flight meal selected.
     */
    NO_MEAL,

    /**
     * A vegetarian meal option.
     */
    VEGETARIAN,

    /**
     * A chicken-based meal option.
     */
    CHICKEN,

    /**
     * A beef-based meal option.
     */
    BEEF
}
