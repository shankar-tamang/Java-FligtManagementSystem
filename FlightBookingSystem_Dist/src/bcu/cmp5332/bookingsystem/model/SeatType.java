package bcu.cmp5332.bookingsystem.model;

/**
 * An enumeration of seat classes for flights,
 * defining Economy, Business, or First class.
 */
public enum SeatType {
    /**
     * Economy class seat.
     */
    ECONOMY,

    /**
     * Business class seat.
     */
    BUSINESS,

    /**
     * First class seat.
     */
    FIRST
}
